public class Vagao {
    private int id;
    private int capacidadeMaxCarga;

    public Vagao(int id, int capacidadeMaxCarga) {
        this.id = id;
        this.capacidadeMaxCarga = capacidadeMaxCarga;
    }

    public int getId() {
        return id;
    }

    public int getCapacidadeMaxCarga() {
        return capacidadeMaxCarga;
    }
}
